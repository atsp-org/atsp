*
      WRITE(IWRITE,2)
      CALL CFGO1(NCFG,MAXORB,IAJCMP,LJCOMP,NJCOMP,NOCCSH,
     :            NELCSH,NOCORB,J1QNRD,NCD2,INPUT)
