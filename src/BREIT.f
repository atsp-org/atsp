   12 IF(IBUG2-1) 13,13,16
*
*     PRINT-OUT OF VALUES IN NJSYM  IF IBUG3=1
*
   16 IF(IBUG3.NE.1) WRITE(IWRITE,14)(J1(J),J=1,MLIMIT)
C
C     IF ITST1.NE.2 OR ITST2.NE.2 THEN NJGRAF IS BEING CALLED, SO SET
