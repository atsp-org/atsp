*     DETERMINE THE SIGN ASSOCIATED WITH ANGULAR MOMENTUM PHASE
*          CONVENTIONS
*
   22 ISTART=1
   23 IEND=NCFG
      SIGNFA(ISTART)=ONE
      LBASE=0
      N1=NOCCSH(ISTART)
      DO 7 I=1,N1
      N2=NOCORB(I,ISTART)
      LBASE=LBASE+NELCSH(I,ISTART)*LJCOMP(N2)
    7 CONTINUE
      ISP1=ISTART+1
      DO 13 N=ISP1,IEND
      LPHASE=0
      N1=NOCCSH(N)
      DO 4 I=1,N1
      N2=NOCORB(I,N)
      LPHASE=LPHASE+NELCSH(I,N)*LJCOMP(N2)
    4 CONTINUE
      LPHASE=(LPHASE-LBASE)/2
      IF((LPHASE-LPHASE/2*2).EQ.0) GO TO 5
      SIGNFA(N)=-ONE
      GO TO 13
    5 SIGNFA(N)=ONE
   13 CONTINUE
   21 CONTINUE
      RETURN
      END
